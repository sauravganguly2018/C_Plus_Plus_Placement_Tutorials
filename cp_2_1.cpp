#include<iostream>
using namespace std;

int main(){
    int a;
    cout<<"size of int is "<<sizeof(a)<<" bytes\n";

    char b;
    cout<<"size of char is "<<sizeof(b)<<" bytes\n";

    float c;
    cout<<"size of float is "<<sizeof(c)<<" bytes\n";

    bool d;
    cout<<"size of bool is "<<sizeof(d)<<" bytes\n";

    short int si;
    cout<<"size of shortint is "<<sizeof(si)<<" bytes\n";

    long int li;
    cout<<"size of longint is "<<sizeof(li)<<" bytes\n";
    return 0;
}